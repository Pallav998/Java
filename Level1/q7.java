//Create a program that uses different data types and prints their values

public class q7 {
   public static void main(String[] args) {
    byte a = 1; //byte = 8bit
     System.out.println("a is :"+a);
    char c = 'A'; //char = 2byte
     System.out.println("c is :"+c);
    short s = 23; // short = 2byte
     System.out.println("s is :"+s);
    int i =12; //int =4byte
     System.out.println("i is :"+i);
    long l =127 ;//long = 8byte
     System.out.println("l is :"+l);
    float f = 1.45f; //float = 4byte
    System.out.println("f is :"+f);
    double d = 23.444;//double = 8byte
    System.out.println("d is :"+d);
   } 
}
