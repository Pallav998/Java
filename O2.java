//Relationship Operator it will return true of false.
public class O2 {
   public static void main(String[] args) {
    int a =98;
    int b =84;

    if(a==b)
    {
        System.out.println(a==b);
    }
    else if(a!=b)
    {
        System.out.println(a!=b);
    }
    else if(a<b)
    {
        System.out.println(a<b);
    }
    else if(a>b)
    {
        System.out.println(a>b);
    }
    else if(a<=b)
    {
        System.out.println(a<=b);
    }
    else if (a>=b)
    {
        System.out.println(a>=b);
    }
    else{
        System.out.println("Nothing");
    }
   } 
}
