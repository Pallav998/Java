//Explain Public static void main:

public class Q1 {
   public static void main(String[] args) {
    System.out.println("Hello Java");
    
   } 
}
// here public is an access modifier which can be accessed by any other class.

//static is a keyword .
//when JVM calls the main method there is no object of the class present so main method needs to be static.

//void is the return type. It indicates that the main method not return anything.It simply perform some actions

//main is the name of the method.without the main method JVM will not execute the program.

//string args[]: It is a method parameter.
// JVM always look for the main method string type with array as a parameter.
